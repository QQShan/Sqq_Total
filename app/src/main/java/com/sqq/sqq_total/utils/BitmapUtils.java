    public static byte[] compressBitmapToByte(String filePath,int reqW,int reqH,int quality,Bitmap.CompressFormat format){
        Bitmap bitmap = getSmallImage(filePath,reqW,reqH);
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        bitmap.compress(format,quality,baos);
        byte[] bytes = baos.toByteArray();
        bitmap.recycle();
        return  bytes;
    }
